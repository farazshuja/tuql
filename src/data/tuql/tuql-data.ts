const letters = 'abtvjHxd*rzs$SDTZegfkqlmnwhy'

export const getPageByString = (str = ''): number => {
  const result = tuqlData.find((d) => d.startsWith(str))
  if (result) {
    return Number(result.split(':')[1]) || 1
  }
  if (str.length <= 1) {
    return 1
  }
  // else case
  const strArr = str.split('')
  const lastLetter = strArr.pop()
  const str1 = strArr.join('')
  const prevIndex = letters.indexOf(lastLetter || '') - 1
  const newStr = prevIndex > -1
    ? `${str1}${letters[prevIndex]}`
    : str1
  return getPageByString(newStr)
}
